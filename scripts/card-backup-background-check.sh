CONFIG_DIR=$(dirname "$0")
CONFIG="${CONFIG_DIR}/config.cfg"
source "$CONFIG"

# Wait for a USB storage device (e.g., a USB flash drive)
STORAGE=$(ls /dev/* | grep "$STORAGE_DEV" | cut -d"/" -f3)
while [ -n "${STORAGE}" ]
do
    sleep 1
    STORAGE=$(ls /dev/* | grep "$STORAGE_DEV" | cut -d"/" -f3)
done

# disable auto shutdown
shutdown -c

echo "Storage has been disconnected"

# card-backup restart
pkill -f card-backup.sh
pkill -f card-backup-run.sh
"${CONFIG_DIR}/card-backup.sh" &